document.write("<h2>For Loop in JS</h2>");
document.write("Q.1 Print the multiplication table of 5 using for loop.")
for (var x=1; x<=10; x++){
document.write("<h3>"+(5*x)+ "</h3>")
}


document.write("Q.2 Print first 5 even numbers.");
for (var x=1; x<=5; x++){
document.write("<h3>"+(2*x)+ "</h3>")
}
